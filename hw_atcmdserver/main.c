int bin2hex(unsigned char* inputString, unsigned char* outputHex, unsigned int inputLen)
{
    
    int v3; // r3
    unsigned int v4; // r5
    
    v3 = 0;
    while ( 1 )
    {
        outputHex += 2;
        if ( v3 >= inputLen )
            break;
        
        v4 = (unsigned char)inputString[v3++];
        *(outputHex - 2) = abcdef[v4 >> 4];
        *(outputHex - 1) = abcdef[v4 & 0xF];
    }
    return 0;
}




